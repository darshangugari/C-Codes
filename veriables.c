#include <stdio.h>
int main()
{
	int a=2;   // integer
	float b=3.35;  /* real integer */
	char d='x';  // character veriable
	printf("the value of a is %d\n",a);   // %d because of integer (2)
	printf("add a and b is %f\n",a+b);  // %f because of real integer (3.35)
	printf("the value of d is %c \n",d);   // %c because of character veriable 'x'
	
	return 0;
}
